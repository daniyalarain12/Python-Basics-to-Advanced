#STRINGS
str1 = "I AM DANIYAL ARAIN."
str2 = 'I AM DANIYAL ARAIN.'
str3 = """I AM DANIYAL ARAIN."""
str4 = '''I AM DANIYAL ARAIN.'''
print(str1)
print(str2)
print(str3)
print(str4)

#ESCAPE SEQUENCE CHARACTERS
string1 = "I AM DANIYAL ARAIN.\nI AM A GOOD STUDENT."   # \n is used for newline. 
string2 = "I AM DANIYAL ARAIN.\tI AM A GOOD STUDENT."   # \t is used for tab (8 spaces).   
print(string1)
print(string2)
